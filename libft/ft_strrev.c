#include "libft.h"

char				*ft_strrev(char *str)
{
	unsigned int	i;
	unsigned int	j;
	char			c;

	i = ft_strlen(str) - 1;
	j = 0;
	while (i > j)
	{
		c = str[i];
		str[i] = str[j];
		str[j] = c;
		i--;
		j++;
	}
	return (str);
}
